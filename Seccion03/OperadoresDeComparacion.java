class OperadoresDeComparacion {
    public static void main(String args[]){
        double sueldo1 = 1000.51558;
        double sueldo2 = 450055.052558;
        System.out.println(sueldo1>sueldo2);
        System.out.println(sueldo1<sueldo2);
        System.out.println(sueldo1>=sueldo2);
        System.out.println(sueldo1<=sueldo2);
        System.out.println(sueldo1!=sueldo2);
        System.out.println(sueldo1==sueldo2);
    }
}